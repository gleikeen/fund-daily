def update_index(report_file, summary_line):
    idx = "index.html"
    with open(idx, "r", encoding="utf-8") as fh:
        content = fh.read()
    # 更新“最新报告”卡片（整体替换，避免硬编码上一期文件名）
    m = re.search(r'<a class="latest" href="[^"]+">.*?</a>', content, re.S)
    if m:
        new_block = (f'<a class="latest" href="{report_file}">\n'
                     f'      <span class="label">最新报告</span>\n'
                     f'      <strong>{RUN_TS_DISPLAY}</strong>\n'
                     f'      <span>{summary_line}</span>\n'
                     f'    </a>')
        content = content[:m.start()] + new_block + content[m.end():]
    # 归档：在 <ul> 首个 <li> 前插入新条目
    new_li = (f'        <li><a href="{report_file}"><time datetime="{RUN_DATE}">{RUN_TS}</time>'
              f'<span class="arrow">查看报告 →</span></a></li>\n')
    content = re.sub(r'(      <ul>\n)', rf'\1{new_li}', content, count=1)
    with open(idx, "w", encoding="utf-8") as fh:
        fh.write(content)
    # README 链接同步（替换首个报告文件名）
    readme = "README.md"
    if os.path.exists(readme):
        with open(readme, "r", encoding="utf-8") as fh:
            rc = fh.read()
        rc = re.sub(r'fund-report-\d{4}-\d{2}-\d{2}-\d{2}\.html', report_file, rc, count=1)
        with open(readme, "w", encoding="utf-8") as fh:
            fh.write(rc)


